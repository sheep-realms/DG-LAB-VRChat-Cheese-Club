def get_preset(name: str) -> list[str] | None:
    """获取指定名称的波形预设"""
    return PRESETS.get(name)


def get_names() -> list[str]:
    """获取所有预设名称"""
    return list(PRESETS.keys())


def get_random_for_second(second: int) -> tuple[str, list[str]]:
    """根据秒数随机返回一个推荐波形 (name, data)"""
    candidates = SECOND_WAVEFORM_MAP.get(second, list(PRESETS.keys()))
    name = random.choice(candidates)
    return name, PRESETS[name]


def get_random() -> tuple[str, list[str]]:
    """随机返回一个波形"""
    name = random.choice(list(PRESETS.keys()))
    return name, PRESETS[name]


def scale_waveform(data: list[str], intensity: int) -> list[str]:
    """缩放波形数据到指定强度 (0-200 UI range → 0-100 hex pulse range)
    格式: 前8字符=carrier[4], 后8字符=pulse[4]
    只缩放pulse值，carrier保持不变。
    Pulse hex max is 0x64 (100) per DG-LAB V3 protocol.
    """
    if not data:
        return data
    # 找到波形中的最大pulse值
    max_pulse = 0
    for entry in data:
        for j in range(4):
            pos = 8 + j * 2
            if pos + 2 <= len(entry):
                try:
                    val = int(entry[pos:pos + 2], 16)
                    max_pulse = max(max_pulse, val)
                except ValueError:
                    pass
    if max_pulse == 0:
        return data

    # UI intensity 0-200 maps to hex pulse 0-100
    target_hex = intensity // 2
    scale = target_hex / max_pulse
    result = []
    for entry in data:
        if len(entry) >= 16:
            carrier_part = entry[:8]
            pulse_part = entry[8:16]
            new_pulse = ""
            for j in range(4):
                pos = j * 2
                try:
                    pulse_val = int(pulse_part[pos:pos + 2], 16)
                    new_val = min(100, max(0, int(pulse_val * scale)))
                    new_pulse += f"{new_val:02X}"
                except ValueError:
                    new_pulse += pulse_part[pos:pos + 2]
            result.append(carrier_part + new_pulse)
        else:
            result.append(entry)
    return result


def loop_waveform(data: list[str], target_count: int) -> list[str]:
    """循环波形数据以达到目标长度"""
    if not data:
        return data
    result = []
    while len(result) < target_count:
        result.extend(data)
    return result[:target_count]
